#!/usr/bin/env node

import fs from "node:fs";
import http from "node:http";
import os from "node:os";
import path from "node:path";

loadEnvFile(path.join(process.cwd(), ".env"));
loadEnvFile(path.join(os.homedir(), ".hermes", ".env"));
loadEnvFile(path.join(process.cwd(), ".env.local"));

const args = parseArgs(process.argv.slice(2));
const baseUrl = trimTrailingSlash(
  args["base-url"] ||
    process.env.THECHAT_BASE_URL ||
    process.env.THECHAT_BACKEND_URL ||
    "http://localhost:3337",
);
const botToken =
  args["bot-token"] ||
  process.env.THECHAT_BOT_TOKEN ||
  process.env.THECHAT_HERMES_BOT_TOKEN;
const holdMs = Number.parseInt(args["hold-ms"] || "1200", 10);
const waitMs = Number.parseInt(args["wait-ms"] || "60000", 10);
const webhookHost = args["webhook-host"] || "127.0.0.1";
const webhookPort = Number.parseInt(args["webhook-port"] || "0", 10);
const webhookPath = args["webhook-path"] || "/thechat-progress-demo";
const shouldComplete = !args["no-complete"];
const keepWebhook = !!args["keep-webhook"];

if (!botToken) {
  fail(
    "Missing bot token. Set THECHAT_BOT_TOKEN or pass --bot-token=bot_...",
  );
}

console.log(`Using TheChat API at ${baseUrl}`);

await request("/hermes-platform/health", { method: "GET" });
const { event, cleanup } = await claimOrReceiveInvocation();

const invocationId = event.invocationId;
console.log(`Claimed invocation ${invocationId}`);

try {
  await emitProgress({
    type: "reasoning.available",
    status: "running",
    preview: "Demo progress: inspecting the project before answering.",
    payload: { text: "Demo progress: inspecting the project before answering." },
  });
  await sleep(holdMs);

  await emitProgress({
    type: "tool.started",
    status: "running",
    toolCallId: "demo-read-1",
    toolName: "read_file",
    label: "Read packages/api/src/services/bot-runtime.ts",
    payload: {
      args: { path: "packages/api/src/services/bot-runtime.ts" },
    },
  });
  await sleep(holdMs);

  await emitProgress({
    type: "tool.completed",
    status: "completed",
    toolCallId: "demo-read-1",
    toolName: "read_file",
    payload: {
      duration: 1.2,
      isError: false,
    },
  });
  await sleep(holdMs);

  await emitProgress({
    type: "tool.started",
    status: "running",
    toolCallId: "demo-shell-1",
    toolName: "shell",
    label: "Shell: pnpm test:api",
    payload: {
      args: { command: "pnpm test:api" },
    },
  });
  await sleep(holdMs);

  await emitProgress({
    type: "tool.completed",
    status: "completed",
    toolCallId: "demo-shell-1",
    toolName: "shell",
    payload: {
      duration: 2.4,
      isError: false,
    },
  });

  if (shouldComplete) {
    await sleep(holdMs);
    await request("/hermes-platform/messages", {
      method: "POST",
      body: {
        invocationId,
        content:
          "Hermes progress demo complete. This response was generated by scripts/hermes-progress-demo.mjs, not by an AI model.",
        complete: false,
      },
    });
    await request(`/hermes-platform/invocations/${invocationId}/completed`, {
      method: "POST",
      body: { reason: "Hermes progress demo completed" },
    });
    console.log("Completed invocation with a demo response.");
  } else {
    console.log(
      "Left invocation running because --no-complete was passed. Complete or cancel it from the API when done inspecting the UI.",
    );
  }
} finally {
  await cleanup();
}

async function emitProgress(body) {
  await request(`/hermes-platform/invocations/${invocationId}/progress`, {
    method: "POST",
    body,
  });
  console.log(`Sent ${body.type}${body.toolName ? ` for ${body.toolName}` : ""}`);
}

async function claimOrReceiveInvocation() {
  console.log("Checking for an already queued Hermes invocation...");
  const claimed = await request("/hermes-platform/events?limit=1", { method: "GET" });
  const queued = claimed.events?.[0];
  if (queued) return { event: queued, cleanup: async () => {} };

  console.log("No queued polling invocation found.");
  console.log("Starting a temporary webhook receiver for webhook-mode bots...");
  const receiver = await startWebhookReceiver();
  const webhookUrl = `http://${webhookHost}:${receiver.port}${webhookPath}`;
  await request("/bots/me/webhook", {
    method: "POST",
    body: { url: webhookUrl },
  });
  console.log(`Temporary webhook registered at ${webhookUrl}`);
  console.log("Now send a new message to the Hermes bot in TheChat.");

  const cleanup = async () => {
    receiver.close();
    if (keepWebhook) return;
    try {
      await request("/bots/me/webhook", { method: "DELETE" });
      console.log("Cleared the temporary webhook registration.");
    } catch (error) {
      console.warn("Failed to clear temporary webhook registration:", error.message);
    }
  };

  try {
    const event = await withTimeout(
      Promise.race([receiver.nextEvent(), pollForQueuedInvocation()]),
      waitMs,
      `Timed out waiting for a Hermes invocation after ${waitMs}ms. Make sure TheChat API is running, then send a new message to the Hermes bot.`,
    );
    return { event, cleanup };
  } catch (error) {
    await cleanup();
    throw error;
  }
}

async function pollForQueuedInvocation() {
  while (true) {
    await sleep(750);
    const claimed = await request("/hermes-platform/events?limit=1", { method: "GET" });
    const queued = claimed.events?.[0];
    if (queued) return queued;
  }
}

async function startWebhookReceiver() {
  let resolveEvent;
  let rejectEvent;
  const eventPromise = new Promise((resolve, reject) => {
    resolveEvent = resolve;
    rejectEvent = reject;
  });

  const server = http.createServer(async (req, res) => {
    if (req.method !== "POST" || req.url !== webhookPath) {
      res.writeHead(404, { "Content-Type": "application/json" });
      res.end(JSON.stringify({ error: "Not found" }));
      return;
    }
    try {
      const raw = await readRequestBody(req);
      const payload = JSON.parse(raw || "{}");
      const event = payload.event && typeof payload.event === "object" ? payload.event : payload;
      if (!event.invocationId) throw new Error("Webhook payload missing invocationId");
      resolveEvent(event);
      res.writeHead(200, { "Content-Type": "application/json" });
      res.end(JSON.stringify({ ok: true }));
    } catch (error) {
      rejectEvent(error);
      res.writeHead(400, { "Content-Type": "application/json" });
      res.end(JSON.stringify({ error: error.message }));
    }
  });

  await new Promise((resolve, reject) => {
    server.once("error", reject);
    server.listen(webhookPort, webhookHost, resolve);
  });
  const address = server.address();
  const port = typeof address === "object" && address ? address.port : webhookPort;
  return {
    port,
    nextEvent() {
      return eventPromise;
    },
    close() {
      server.close();
    },
  };
}

function readRequestBody(req) {
  return new Promise((resolve, reject) => {
    const chunks = [];
    req.on("data", (chunk) => chunks.push(chunk));
    req.on("error", reject);
    req.on("end", () => resolve(Buffer.concat(chunks).toString("utf8")));
  });
}

async function request(endpoint, options) {
  const response = await fetch(`${baseUrl}${endpoint}`, {
    method: options.method,
    headers: {
      Authorization: `Bearer ${botToken}`,
      ...(options.body ? { "Content-Type": "application/json" } : {}),
    },
    body: options.body ? JSON.stringify(options.body) : undefined,
  });
  const text = await response.text();
  const body = text ? JSON.parse(text) : {};
  if (!response.ok) {
    throw new Error(
      `${options.method} ${endpoint} failed with HTTP ${response.status}: ${JSON.stringify(body)}`,
    );
  }
  return body;
}

function parseArgs(argv) {
  const parsed = {};
  for (const arg of argv) {
    if (!arg.startsWith("--")) continue;
    const [key, ...valueParts] = arg.slice(2).split("=");
    parsed[key] = valueParts.length > 0 ? valueParts.join("=") : "true";
  }
  return parsed;
}

function loadEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return;
  const content = fs.readFileSync(filePath, "utf8");
  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) continue;
    const match = line.match(/^(?:export\s+)?([A-Za-z_][A-Za-z0-9_]*)=(.*)$/);
    if (!match) continue;
    const [, key, rawValue] = match;
    if (process.env[key] !== undefined) continue;
    process.env[key] = unquote(rawValue.trim());
  }
}

function unquote(value) {
  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    return value.slice(1, -1);
  }
  return value.replace(/\s+#.*$/, "");
}

function trimTrailingSlash(value) {
  return String(value || "").replace(/\/+$/, "");
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, Number.isFinite(ms) ? ms : 0));
}

function withTimeout(promise, timeoutMs, message) {
  if (!Number.isFinite(timeoutMs) || timeoutMs <= 0) return promise;
  let timer;
  return Promise.race([
    promise,
    new Promise((_, reject) => {
      timer = setTimeout(() => reject(new Error(message)), timeoutMs);
    }),
  ]).finally(() => clearTimeout(timer));
}

function fail(message) {
  console.error(message);
  process.exit(1);
}
